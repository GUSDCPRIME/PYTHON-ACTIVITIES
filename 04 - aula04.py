# operadores aritméticos:
v1 = float(input('digite o primeiro valor: '))
v2 = float(input('digite o segundo valor: '))
# Soma: 
soma = v1 + v2
print(f'A soma =', soma)

# subtração
sub = v1 - v2
print(f'A subtração é =', sub)

# multiplicação:
mult = v1 * v2
print(f'A multiplicação é =', mult)

# divisão:
div = v1 / v2
print(f'A divisão é =', div)

# Divisão Inteira:
div_inteira = v1 // v2
print(f'A divisão inteira é =', div_inteira)

# Resto da divisão:
resto = v1 % v2
print(f'O resto da divisão é =', resto)

# exponenciação:
exp = v1 ** v2
print(f'A exponenciação é =', exp)